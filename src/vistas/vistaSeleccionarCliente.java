    private void btnBuscarPorDniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarPorDniActionPerformed
        // TODO add your handling code here:
        String dniStr = txtDni.getText().trim();
        if (dniStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese un DNI para buscar.");
            return;
        }
        try {
            int dni = Integer.parseInt(dniStr);
            ArrayList<Cliente> resultado = new ArrayList<>();
            for (Cliente c : clienteData.listarClientes()) {
                if (c.getDni() == dni) {
                    resultado.add(c);
                }
            }
            if (resultado.isEmpty()) {
                JOptionPane.showMessageDialog(this, "No se encontrs cliente con ese DNI.");
            }
            cargarTablaConLista(resultado);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "El DNI debe ser un nmero.");
        }
    }//GEN-LAST:event_btnBuscarPorDniActionPerformed

    private void btnBuscarPorIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarPorIdActionPerformed
        // TODO add your handling code here:
        String idStr = txtId.getText().trim();
        if (idStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese un ID para buscar.");
            return;
        }
        try {
            int id = Integer.parseInt(idStr);
            Cliente c = clienteData.buscarCliente(id);
            ArrayList<Cliente> resultado = new ArrayList<>();
            if (c != null) {
                resultado.add(c);
            } else {
                JOptionPane.showMessageDialog(this, "No se encontró cliente con ese ID.");
            }
            cargarTablaConLista(resultado);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "El ID debe ser un número.");
        }
    }//GEN-LAST:event_btnBuscarPorIdActionPerformed

    private void btnSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarActionPerformed
        // TODO add your handling code here:
        int fila = tableClientes.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un cliente de la tabla.");
            return;
        }

        int id = (int) tableClientes.getValueAt(fila, 0);
        clienteSeleccionado = clienteData.buscarCliente(id);
        if (clienteSeleccionado == null) {
            JOptionPane.showMessageDialog(this, "Error al obtener el cliente seleccionado.");
            return;
        }
        
        
        // Cerrar la ventana y retornar el cliente
        
        controlSesion.mostrarDatosCliente(clienteSeleccionado);
        
        this.dispose();
    }//GEN-LAST:event_btnSeleccionarActionPerformed


